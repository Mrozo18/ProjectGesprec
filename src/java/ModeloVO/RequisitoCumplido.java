public class RequisitoCumplido {
    private int idRequisitoCumplido;
    private int cumplido;
    private String nombreRequisito;

    public int getIdRequisitoCumplido() {
        return idRequisitoCumplido;
    }

    public void setIdRequisitoCumplido(int idRequisitoCumplido) {
        this.idRequisitoCumplido = idRequisitoCumplido;
    }

    public int getCumplido() {
        return cumplido;
    }

    public void setCumplido(int cumplido) {
        this.cumplido = cumplido;
    }

    public String getNombreRequisito() {
        return nombreRequisito;
    }

    public void setNombreRequisito(String nombreRequisito) {
        this.nombreRequisito = nombreRequisito;
    }
    
    
}
